val_90 = base_val + 0.90 * delta;
t10 = t(find(kalman >= val_10 & t >= 4.5, 1, 'first'));
t90 = t(find(kalman >= val_90 & t >= 4.5, 1, 'first'));
t_rise = t90 - t10;

settle_band_upper = final_val + 0.02 * abs(delta);
settle_band_lower = final_val - 0.02 * abs(delta);
idx_outside = find((kalman < settle_band_lower | kalman > settle_band_upper) & t >= t_start_action);
t_settle_time = t(idx_outside(end)); 
t_settle = t_settle_time - t_start_action; 

