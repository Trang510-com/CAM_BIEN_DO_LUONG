%% 2. TÍNH TOÁN THEO MỐC THỰC TẾ (Cập nhật mốc t = 5.00s dựa trên log mới)
t_start_action = 5.00; 
idx_stable = (t >= 7.5 & t <= 9.9);
final_val = mean(kalman(idx_stable)); 
base_val = mean(kalman(t < 4.7)); % Lấy nền trước khi tín hiệu tăng mạnh
delta = final_val - base_val;

