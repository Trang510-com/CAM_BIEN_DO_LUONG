%% 2. TÍNH TOÁN THEO MỐC THỰC TẾ ĐÃ CẬP NHẬT (t_start_action = 25.56s)
% Dựa trên tập dữ liệu, lệnh bắt đầu thực tế nhấc lên cao ổn định từ mốc 25.56s
