%% 2. TÍNH TOÁN THEO MỐC THỰC TẾ (t = 5.00s)
t_start_action = 5.00; 
% Chọn vùng ổn định tĩnh trước khi tín hiệu lao dốc hẳn xuống (từ 5.50s đến 9.30s)
idx_stable = (t >= 5.50 & t <= 9.30);
final_val = mean(kalman(idx_stable)); 
base_val = mean(kalman(t < 4.50)); % Tính nền tĩnh ban đầu lúc chưa nhấc tay
delta = final_val - base_val;

