val_90 = base_val + 0.90 * delta;
t10 = t(find(kalman >= val_10 & t >= 4.50, 1, 'first'));
t90 = t(find(kalman >= val_90 & t >= 4.50, 1, 'first'));
t_rise = t90 - t10;

settle_band_upper = final_val + 0.02 * abs(delta);
settle_band_lower = final_val - 0.02 * abs(delta);

% Tìm thời điểm cuối cùng vượt ra ngoài băng thông 2% trong pha nâng tay (trước 9.30s)
idx_outside = find((kalman < settle_band_lower | kalman > settle_band_upper) & t >= t_start_action & t <= 9.30);
if ~isempty(idx_outside)
    t_settle_time = t(idx_outside(end)); 
    t_settle = t_settle_time - t_start_action; 
else
    t_settle_time = t_start_action;
    t_settle = 0;
end

